package Entities;

public class Calculator {

    public static double somaCalculator(double num1, double num2){
        return  num1 + num2;
    }
    public static double subCalculator(double num1, double num2){
        return num1 - num2;
}
    public static double divCalculator(double num1, double num2) {
        return num1 / num2;
    }
    public static double multCalculator(double num1, double num2) {
        return num1 * num2;
    }
    }